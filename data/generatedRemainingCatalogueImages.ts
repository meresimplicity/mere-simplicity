function slugFromKey(value: string) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1 $2")
    .replace(/\b2\s*k\b/gi, "2k")
    .replace(/\b300\s*d\b/gi, "300d")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/\+/g, "plus")
    .replace(/%/g, "percent")
    .replace(/\b2k\b/g, "two k")
    .replace(/\b300d\b/g, "three hundred d")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
