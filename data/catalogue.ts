export const catalogueProducts: CatalogueProduct[] = catalogueCategories.flatMap((category) =>
  category.products.map((product) => createProduct(category, product))
);

function createProduct(category: CatalogueCategory, seed: ProductSeed): CatalogueProduct {
  const product = typeof seed === "string" ? { name: seed } : seed;
  const subcategory = product.subcategory ?? category.title;

  return {
    id: `${category.id}-${slugify(product.name)}`,
    name: product.name,
    categoryId: category.id,
    category: category.title,
    filter: category.filter,
    subcategory,
    supplier: product.supplier ?? category.supplier,
    brand: product.brand ?? category.brand,
    code: product.code,
    size: product.size,
    colour: product.colour,
    source: product.source ?? category.source,
    tags: createTags([
      product.name,
      category.title,
      category.filter,
      subcategory,
      product.supplier ?? category.supplier,
      product.brand ?? category.brand,
      ...(product.tags ?? []),
    ]),
  };
}

function createTags(values: (string | undefined)[]) {
  const words = values
    .filter(Boolean)
    .flatMap((value) => String(value).split(/[\/,&\- ]+/))
    .map((word) => word.trim().toLowerCase())
    .filter((word) => word.length > 1 && !["and", "for", "with", "the"].includes(word));

  return Array.from(new Set(words)).slice(0, 8);
}

function slugify(value: string) {
  return value
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
